#define DEBUG_TYPE "asm-printer"

namespace {
class RISCVAsmPrinter : public AsmPrinter {
public:
  explicit RISCVAsmPrinter(TargetMachine &TM,
                           std::unique_ptr<MCStreamer> Streamer)
      : AsmPrinter(TM, std::move(Streamer)),
        is64Bit(TM.getTargetTriple().getArch() == llvm::Triple::riscv64) {}

  StringRef getPassName() const override { return "RISCV Assembly Printer"; }

  void EmitInstruction(const MachineInstr *MI) override;

  bool PrintAsmOperand(const MachineInstr *MI, unsigned OpNo,
                       unsigned AsmVariant, const char *ExtraCode,
                       raw_ostream &OS) override;
  bool PrintAsmMemoryOperand(const MachineInstr *MI, unsigned OpNo,
                             unsigned AsmVariant, const char *ExtraCode,
                             raw_ostream &OS) override;

  void EmitStartOfAsmFile(Module &) override;

  void EmitToStreamer(MCStreamer &S, const MCInst &Inst);
  // Auto-generated by tablegen.
  bool emitPseudoExpansionLowering(MCStreamer &OutStreamer,
                                   const MachineInstr *MI);
  // Not yet auto-generated by tablegen.
  bool emitOtherPseudoExpansionLowering(MCStreamer &OutStreamer,
                                        const MachineInstr *MI);

  // Wrapper needed for tblgenned pseudo lowering.
  bool lowerOperand(const MachineOperand &MO, MCOperand &MCOp) const {
    return LowerRISCVMachineOperandToMCOperand(MO, MCOp, *this);
  }
private:
  bool is64Bit;

  RISCVTargetStreamer *getTargetStreamer() {
    MCTargetStreamer *TS = OutStreamer->getTargetStreamer();
    return static_cast<RISCVTargetStreamer *>(TS);
  }
};
}

void RISCVAsmPrinter::EmitStartOfAsmFile(Module &M) {
  AsmPrinter::EmitStartOfAsmFile(M);

  if (!isPositionIndependent())
    getTargetStreamer()->emitDirectiveOptionNoPIC();
  else
    getTargetStreamer()->emitDirectiveOptionPIC();
}

#define GEN_COMPRESS_INSTR
#include "RISCVGenCompressInstEmitter.inc"
void RISCVAsmPrinter::EmitToStreamer(MCStreamer &S, const MCInst &Inst) {
  MCInst CInst;
  bool Res = compressInst(CInst, Inst, *TM.getMCSubtargetInfo(),
                          OutStreamer->getContext());
  AsmPrinter::EmitToStreamer(*OutStreamer, Res ? CInst : Inst);
}

// Simple pseudo-instructions have their lowering (with expansion to real
// instructions) auto-generated.
#include "RISCVGenMCPseudoLowering.inc"

void RISCVAsmPrinter::EmitInstruction(const MachineInstr *MI) {
  // Do any auto-generated pseudo lowerings.
  if (emitPseudoExpansionLowering(*OutStreamer, MI))
    return;

  // Do pseudo-lowerings that can't be auto-generated yet.
  if (emitOtherPseudoExpansionLowering(*OutStreamer, MI))
    return;

  MCInst TmpInst;
  LowerRISCVMachineInstrToMCInst(MI, TmpInst, *this);
  EmitToStreamer(*OutStreamer, TmpInst);
}

bool RISCVAsmPrinter::emitOtherPseudoExpansionLowering(MCStreamer &OutStreamer,
                                                       const MachineInstr *MI) {
  switch (MI->getOpcode()) {
  default:
    return false;
  case RISCV::PseudoAddrPIC: {
    const MachineOperand &GlobalAddrOp = MI->getOperand(1);
    unsigned char TargetFlags = GlobalAddrOp.getTargetFlags();
    assert(
        (TargetFlags == RISCVII::MO_GOT || TargetFlags == RISCVII::MO_PCREL) &&
        "Invalid target flags");
    MCOperand MCGlobalAddrOp;
    lowerOperand(GlobalAddrOp, MCGlobalAddrOp);
    assert(MCGlobalAddrOp.isExpr() &&
           MCGlobalAddrOp.getExpr()->getKind() == MCExpr::SymbolRef &&
           "This operand should have been lowered as a SymbolRef");
    unsigned Opcode =
        TargetFlags == RISCVII::MO_GOT ? RISCV::PseudoLA : RISCV::PseudoLLA;
    MCInst PseudoInst = MCInstBuilder(Opcode)
                       .addReg(MI->getOperand(0).getReg())
                       .addExpr(MCGlobalAddrOp.getExpr());
    EmitToStreamer(OutStreamer, PseudoInst);
    break;
  }
  }
  return true;
}

bool RISCVAsmPrinter::PrintAsmOperand(const MachineInstr *MI, unsigned OpNo,
                                      unsigned AsmVariant,
                                      const char *ExtraCode, raw_ostream &OS) {
  if (AsmVariant != 0)
    report_fatal_error("There are no defined alternate asm variants");

  // First try the generic code, which knows about modifiers like 'c' and 'n'.
  if (!AsmPrinter::PrintAsmOperand(MI, OpNo, AsmVariant, ExtraCode, OS))
    return false;

  if (!ExtraCode) {
    const MachineOperand &MO = MI->getOperand(OpNo);
    switch (MO.getType()) {
    case MachineOperand::MO_Immediate:
      OS << MO.getImm();
      return false;
    case MachineOperand::MO_Register:
      OS << RISCVInstPrinter::getRegisterName(MO.getReg());
      return false;
    default:
      break;
    }
  }

  return true;
}

bool RISCVAsmPrinter::PrintAsmMemoryOperand(const MachineInstr *MI,
                                            unsigned OpNo, unsigned AsmVariant,
                                            const char *ExtraCode,
                                            raw_ostream &OS) {
  if (AsmVariant != 0)
    report_fatal_error("There are no defined alternate asm variants");

  if (!ExtraCode) {
    const MachineOperand &MO = MI->getOperand(OpNo);
    // For now, we only support register memory operands in registers and
    // assume there is no addend
    if (!MO.isReg())
      return true;

    OS << "0(" << RISCVInstPrinter::getRegisterName(MO.getReg()) << ")";
    return false;
  }

  return AsmPrinter::PrintAsmMemoryOperand(MI, OpNo, AsmVariant, ExtraCode, OS);
}

// Force static initialization.
extern "C" void LLVMInitializeRISCVAsmPrinter() {
  RegisterAsmPrinter<RISCVAsmPrinter> X(getTheRISCV32Target());
  RegisterAsmPrinter<RISCVAsmPrinter> Y(getTheRISCV64Target());
}
